
class Vraag {
    private String vraag;
    private String antwoord;

    public Vraag(String vraag) {
        this.vraag = vraag;
    }

    public String getVraag() {
        return vraag;
    }

    public String getAntwoord() {
        return antwoord;
    }

    public void setAntwoord(String antwoord) {
        this.antwoord = antwoord;
    }
}